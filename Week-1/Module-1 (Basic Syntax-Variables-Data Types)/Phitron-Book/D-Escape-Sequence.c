#include <stdio.h>
int main() {
    // new line: \n
    printf("Hello\nWorld!\n");
    
    // horizontal tab: \t
    printf("Raisul Islam\n");
    printf("Raisul\tIslam\n");

    // double quote: \" \"
    printf("\"Bangladesh\" is a beautiful country.\n");

    // single quote: \' \'
    printf("\'Bangladesh\' is a beautiful country.\n");

    // backslash: 
    printf("Raisul\\Sharmin\n");

    // percentage: %
    printf("Give 100%% force.\n");

    return 0;
}