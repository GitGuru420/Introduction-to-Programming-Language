#include <stdio.h>
int main() {
    // variable declaration 
    // syntax: data_type variable_name;
    int age;
    float pi;
    char letter;

    // assign variable value
    // syntax: variable_name = value;
    age = 23;
    pi = 3.1416;
    letter = 'R';

    // accessing variable value
    printf("Age: %d\n", age);
    printf("PI: %f\n", pi);
    printf("Letter: %c\n", letter);

    return 0;
}